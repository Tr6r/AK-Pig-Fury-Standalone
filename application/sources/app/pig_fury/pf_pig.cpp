void pf_pig::init() {
	hp_ = MAX_HP;
	pig_st_ = PF_PIG_ST_NONE;
	atk_st_ = PF_PIG_ATTACK_NONE;
	pos_x_ = PIG_POS_X;
	pos_y_ = PIG_POS_Y;
	width_ = PIG_WIDTH;
	height_ = PIG_HEIGHT;
	char_st_ = PF_CHAR_ST_IDLE;
	dir_ = RIGHT;
}

void pf_pig::update()
{
	if (pig_st_ == PF_PIG_ST_ATK)
	{
		if (atk_dur_tick_ > 0) atk_dur_tick_--;
		else pig_st_ = PF_PIG_ST_NONE;
	}
}

void pf_pig::render() {
	switch (pig_st_) {
		case PF_PIG_ST_NONE: {
			if (dir_ == LEFT) view_render.drawBitmap(PIG_POS_X, PIG_POS_Y, pig_idle_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else view_render.drawBitmap(PIG_POS_X, PIG_POS_Y, pig_idle_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		case PF_PIG_ST_ATK: {
			if (dir_ == LEFT) view_render.drawBitmap(PIG_POS_X, PIG_POS_Y, pig_atk_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else view_render.drawBitmap(PIG_POS_X, PIG_POS_Y, pig_atk_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		default:
			break;
	}
}

void pf_pig::attack(pf_char_dir dir) {
	dir_ = dir;
	switch (pig_st_) {
		case PF_PIG_ST_NONE:
		case PF_PIG_ST_ATK:{
			pig_st_ = PF_PIG_ST_ATK;
			if (dir == LEFT) atk_st_ = PF_PIG_ATTACK_PUNCH_LEFT;
			else atk_st_ = PF_PIG_ATTACK_PUNCH_RIGHT;
			atk_dur_tick_ = 2;
			break;
		}
		default:
			break;
	}
}
