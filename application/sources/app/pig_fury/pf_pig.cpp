void pf_pig::init(pf_char_dir dir) {
	hp_ = MAX_HP;
	pig_st_ = PF_PIG_ST_NONE;
	atk_st_ = PF_PIG_ATTACK_NONE;
	pos_x_ = PIG_POS_X;
	pos_y_ = PIG_POS_Y;
	width_ = PIG_WIDTH;
	height_ = PIG_HEIGHT;
	atk_dur_tick_ = 0;
	char_st_ = PF_CHAR_ST_IDLE;
	dir_ = dir;
}

void pf_pig::update() {
	update_atk();
	update_st();
}

void pf_pig::render() {
	render_atk();
	render_st();
}

void pf_pig::attack(pf_char_dir dir) {
	dir_ = dir;
	if (pig_st_ == PF_PIG_ST_JUMP || pig_st_ == PF_PIG_ST_FALL) {
		atk_st_ = PF_PIG_ATTACK_ST_JUMP_KICK;
		atk_dur_tick_ = PIG_ATK_FRAME_TICK;
		return;
	}
	switch (atk_st_) {
		case PF_PIG_ATTACK_NONE: {
			atk_st_ = PF_PIG_ATTACK_PUNCH_1;
			atk_dur_tick_ = PIG_ATK_FRAME_TICK;
			break;
		}
		case PF_PIG_ATTACK_PUNCH_1: {
			atk_st_ = PF_PIG_ATTACK_PUNCH_2;
			atk_dur_tick_ = PIG_ATK_FRAME_TICK;
			break;
		}
		case PF_PIG_ATTACK_PUNCH_2: {
			atk_st_ = PF_PIG_ATTACK_PUNCH_3;
			atk_dur_tick_ = PIG_ATK_FRAME_TICK;
			break;
		}
		case PF_PIG_ATTACK_PUNCH_3: {
			atk_st_ = PF_PIG_ATTACK_PUNCH_1;
			atk_dur_tick_ = PIG_ATK_FRAME_TICK;
			break;
		}
		default:
			break;
	}
}

void pf_pig::render_atk() {
	switch (atk_st_) {
		case PF_PIG_ATTACK_NONE: {
			if (pig_st_ != PF_PIG_ST_NONE) break;
			if (dir_ == PF_CHAR_DIR_LEFT)
				view_render.drawBitmap(pos_x_, pos_y_, pig_idle_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else
				view_render.drawBitmap(pos_x_, pos_y_, pig_idle_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		case PF_PIG_ATTACK_PUNCH_1: {
			if (dir_ == PF_CHAR_DIR_LEFT)
				view_render.drawBitmap(pos_x_, pos_y_, pig_atk_1_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else
				view_render.drawBitmap(pos_x_, pos_y_, pig_atk_1_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		case PF_PIG_ATTACK_PUNCH_2: {
			if (dir_ == PF_CHAR_DIR_LEFT)
				view_render.drawBitmap(pos_x_, pos_y_, pig_atk_2_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else
				view_render.drawBitmap(pos_x_, pos_y_, pig_atk_2_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		case PF_PIG_ATTACK_PUNCH_3: {
			if (dir_ == PF_CHAR_DIR_LEFT)
				view_render.drawBitmap(pos_x_, pos_y_, pig_atk_3_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else
				view_render.drawBitmap(pos_x_, pos_y_, pig_atk_3_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		case PF_PIG_ATTACK_ST_JUMP_KICK: {
			if (dir_ == PF_CHAR_DIR_LEFT)
				view_render.drawBitmap(pos_x_, pos_y_, pig_kick_left_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			else
				view_render.drawBitmap(pos_x_, pos_y_, pig_kick_right_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		}
		default:
			break;
	}
}

void pf_pig::update_atk() {
	if (atk_st_ != PF_PIG_ATTACK_NONE) {
		if (atk_dur_tick_ > 0) atk_dur_tick_--;
		else atk_st_ = PF_PIG_ATTACK_NONE;
	}
}

void pf_pig::update_st()
{
	switch (pig_st_)
	{
		case PF_PIG_ST_JUMP:
		{
			pos_y_ -= PIG_UPDATE_STEP_PIXEL;

			if (pos_y_ <= (PIG_POS_Y - PIG_JUMP_MAX_HEIGHT))
			{
				pos_y_ = PIG_POS_Y - PIG_JUMP_MAX_HEIGHT;
				pig_st_ = PF_PIG_ST_FALL;
			}
			break;
		}

		case PF_PIG_ST_FALL:
		{
			pos_y_ += PIG_UPDATE_STEP_PIXEL;

			if (pos_y_ >= PIG_POS_Y)
			{
				pos_y_ = PIG_POS_Y;
				pig_st_ = PF_PIG_ST_NONE;
				atk_st_ = PF_PIG_ATTACK_NONE;
			}
			break;
		}

		default:
			break;
	}
}

void pf_pig::render_st()
{
	if (atk_st_ == PF_PIG_ATTACK_NONE)
	{
		switch (pig_st_)
		{
		case PF_PIG_ST_JUMP:
			view_render.drawBitmap(pos_x_, pos_y_, pig_jump_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		case PF_PIG_ST_FALL:
			view_render.drawBitmap(pos_x_, pos_y_, pig_fall_bitmap, PIG_WIDTH, PIG_HEIGHT, WHITE);
			break;
		default:
			break;
		}
	}
}

void pf_pig::jump()
{
	if (pig_st_ == PF_PIG_ST_FALL) return;
	pig_st_ = PF_PIG_ST_JUMP;
}
