enum pf_wolf_basic_move_st : uint8_t {
	PF_WOLF_BASIC_MOVE_1,
	PF_WOLF_BASIC_MOVE_2,
	PF_WOLF_BASIC_MOVE_3,
	PF_WOLF_BASIC_MOVE_4,
};

class pf_wolf_basic : public pf_enemy {
public:
	pf_wolf_basic() {hp_ = 0; st_ = PF_ENEMY_ST_DEAD;}
	~pf_wolf_basic() override = default;

	void init(pf_char_dir dir) override;
	void update() override;
	void render() override;
	void attack() override;
	void take_damage() override;

	// Helper
	inline void update_move_state() {
		anim_duration_tick_ --;
		if(anim_duration_tick_ == 0) {
			move_st_ = static_cast<pf_wolf_basic_move_st>((static_cast<uint8_t>(move_st_) + 1) % 4);
			anim_duration_tick_ = PF_WOLF_BASIC_MOVE_FRAME_TICK;
		}
	}

private:
	void update_hit();
	void update_move();
	void update_attack();
	void update_dead();
	void update_knockback();
	void render_hit();
	void render_move();
	void render_attack();
	void render_knockback();
	void render_dead();
};

